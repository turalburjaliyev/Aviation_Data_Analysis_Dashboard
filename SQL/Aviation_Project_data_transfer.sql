-- 1. AC_BASIC
CREATE TABLE AC_BASIC (
    NAME VARCHAR2(255) PRIMARY KEY,
    URL VARCHAR2(500),
    INFO_AVIONICS CLOB,
    INFO_ENGINE VARCHAR2(255),
    INFO_ENGINE_TYPE VARCHAR2(100),
    INFO_WING_TIPS VARCHAR2(100),
    INFO_SEATS_TOTAL NUMBER,
    INFO_SEATS_ECONOMY NUMBER,
    INFO_SEATS_BUSINESS NUMBER,
    INFO_SEATS_FIRST NUMBER
);

-- 2. AC_RANGE_ALT
