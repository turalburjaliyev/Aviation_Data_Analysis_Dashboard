-- 1. AIRLINE_IDENTITY (Ana Cədvəl)
CREATE TABLE AL_IDENTITY (
    AIRLINE_NAME VARCHAR2(255) PRIMARY KEY,
    TICKER VARCHAR2(20),
    CURRENCY VARCHAR2(10),
    EXCHANGE VARCHAR2(50)
);

-- 2. AIRLINE_FINANCE (Maliyyə göstəriciləri)
CREATE TABLE AL_FINANCE (
    AIRLINE_NAME VARCHAR2(255),
    PROFIT_MARGINS NUMBER,
    OP_MARGINS NUMBER,
    ROA NUMBER,
    ROE NUMBER,
    DEBT_TO_EQUITY NUMBER,
    TOTAL_CASH NUMBER,
    TOTAL_DEBT NUMBER,
    REVENUE_TTM NUMBER,
    EBITDA_TTM NUMBER,
    CONSTRAINT FK_AL_FINANCE FOREIGN KEY (AIRLINE_NAME) REFERENCES AL_IDENTITY(AIRLINE_NAME)
);

-- 3. AIRLINE_MARKET (Bazar göstəriciləri)
CREATE TABLE AL_MARKET (
    AIRLINE_NAME VARCHAR2(255),
    MARKET_CAP NUMBER,
    ENTERPRISE_VALUE NUMBER,
    TRAILING_PE NUMBER,
    FORWARD_PE NUMBER,
    PRICE_TO_BOOK NUMBER,
    CONSTRAINT FK_AL_MARKET FOREIGN KEY (AIRLINE_NAME) REFERENCES AL_IDENTITY(AIRLINE_NAME)
);


-- 1. Səhm Qiymətləri (AL_STOCK_PRICES)
CREATE TABLE AL_STOCK_PRICES (
    TRADE_DATE  DATE,
    TICKER      VARCHAR2(20),
    OPEN_PRICE  NUMBER,
    HIGH_PRICE  NUMBER,
    LOW_PRICE   NUMBER,
    CLOSE_PRICE NUMBER,
    VOLUME      NUMBER,
    -- Composite primary key: eyni tarixdə eyni ticker-dən iki sətir ola bilməz
    PRIMARY KEY (TRADE_DATE, TICKER)
);

-- 2. Səhm Analizi (AL_STOCK_ANALYSIS)
CREATE TABLE AL_STOCK_ANALYSIS (
    TRADE_DATE      DATE,
    TICKER          VARCHAR2(20),
    ADJ_CLOSE       NUMBER,
    DAILY_RETURN    NUMBER,
    VOLATILITY_30D  NUMBER, -- ROLLING_VOLATILITY_30D qısaldıldı
    PRIMARY KEY (TRADE_DATE, TICKER)
);


-- 1. Neft Qiymətləri Əsas (OIL_PRICES_MAIN)
CREATE TABLE OIL_PRICES_MAIN (
    TRADE_DATE      DATE,
    COMMODITY_NAME  VARCHAR2(100),
    TICKER          VARCHAR2(20),
    OPEN_PRICE      NUMBER,
    HIGH_PRICE      NUMBER,
    LOW_PRICE       NUMBER,
    CLOSE_PRICE     NUMBER,
    VOLUME          NUMBER,
    PRIMARY KEY (TRADE_DATE, TICKER)
);

-- 2. Neft Qiymətləri Analizi (OIL_PRICES_ANALYSIS)
CREATE TABLE OIL_PRICES_ANALYSIS (
    TRADE_DATE      DATE,
    TICKER          VARCHAR2(20),
    ADJ_CLOSE       NUMBER,
    DAILY_RETURN    NUMBER,
    VOLATILITY_30D  NUMBER, -- ROLLING_VOLATILITY_30D qısaldıldı
    PRIMARY KEY (TRADE_DATE, TICKER)
);

-- 1. Aviaşirkət Donanması (AL_FLEET)
CREATE TABLE AL_FLEET (
    AIRLINE_NAME    VARCHAR2(255),
    AIRCRAFT_TYPE   VARCHAR2(255),
    IN_SERVICE      NUMBER,
    PARKED          NUMBER,
    TOTAL_CURRENT   NUMBER,
    FUTURE_FLEET    NUMBER, -- FUTURE qısaldıldı
    HISTORIC        NUMBER,
    TOTAL_ALL_TIME  NUMBER,
    AVG_AGE         NUMBER, -- AVG_AGE_NUMERIC qısaldıldı
    CONSTRAINT FK_AL_FLEET FOREIGN KEY (AIRLINE_NAME) REFERENCES AL_IDENTITY(AIRLINE_NAME)
);

-- 2. Gəlir Metrikaları (AL_INCOME_METRICS)
CREATE TABLE AL_INCOME_METRICS (
    AIRLINE_NAME     VARCHAR2(255),
    TICKER           VARCHAR2(20),
    PERIOD_END       DATE,
    COST_OF_REVENUE  NUMBER,
    EBITDA           NUMBER,
    NET_INC          NUMBER, -- NET_INCOME qısaldıldı
    OP_EXPENSE       NUMBER, -- OPERATING_EXPENSE qısaldıldı
    OP_INCOME        NUMBER, -- OPERATING_INCOME qısaldıldı
    TOTAL_REVENUE    NUMBER,
    CONSTRAINT FK_AL_INCOME FOREIGN KEY (AIRLINE_NAME) REFERENCES AL_IDENTITY(AIRLINE_NAME)
);

-- 3. Ümumi Metadata (AL_METADATA)
-- Qeyd: Bu cədvəl çox böyükdür (bütün dünya şirkətləri), ona görə FK qoymuruq
