    PERF_RANGE_MI NUMBER,
    PERF_LANDING_DIST_M NUMBER,
    PERF_LANDING_DIST_FT NUMBER,
    PERF_TAKEOFF_DIST_M NUMBER,
    PERF_TAKEOFF_DIST_FT NUMBER,
